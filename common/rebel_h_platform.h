#define BOOT_DONE(cpu)					(BIT(BOOT_##cpu##_STEP_SIZE) - 1)
#define _BOOT_GET_MASK(size, offset)	(~(((1 << (size)) - 1) << (offset)))

#define CP0_BOOT_DONE_STATE	\
	((BOOT_CP0_FREERTOS << BOOT_CP0_BIN_OFFSET) | (BOOT_DONE(CP0) << BOOT_CP0_STEP_OFFSET))
#define CP1_BOOT_DONE_STATE	\
	((BOOT_CP1_FREERTOS << BOOT_CP1_BIN_OFFSET) | (BOOT_DONE(CP1) << BOOT_CP1_STEP_OFFSET))
#define CM7_BOOT_DONE_STATE	\
	((BOOT_TBOOT_P0 << BOOT_CM7_BIN_OFFSET) | (BOOT_DONE(CM7) << BOOT_CM7_STEP_OFFSET))
#define ROT_BOOT_DONE_STATE	\
	((BOOT_ROT_MW << BOOT_ROT_BIN_OFFSET) | (BOOT_DONE(ROT) << BOOT_ROT_STEP_OFFSET))

#define BOOT_DONE_STATE		\
	(CP0_BOOT_DONE_STATE |	\
	 CP1_BOOT_DONE_STATE |	\
	(IS_CM7 ? CM7_BOOT_DONE_STATE : 0) |	\
	(IS_NORMAL_BOOT ? 0 : ROT_BOOT_DONE_STATE))

#define CHECK_BOOT_DONE(val)											\
	(((val) & BOOT_DONE_STATE) == BOOT_DONE_STATE ? "_DONE" : "_FAIL")


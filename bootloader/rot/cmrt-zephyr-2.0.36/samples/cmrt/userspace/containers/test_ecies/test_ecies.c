#define BUFSZ 4096
#define MAX_HASH_SIZE 64

#define MATCH(argp, argl, lstr) \
	((argl == sizeof(lstr)-1) && (memcmp(argp, lstr, sizeof(lstr)-1) == 0))

/* Global buffer for receive and response messages from SIC. */
alignas(4) uint8_t buffer[BUFSZ];
alignas(4) uint8_t buffer_tmp[BUFSZ];

cmrt_ecies_t ecies = CMRT_INVALID_HANDLE;


static void print_usage(void)
{
	LOG_INF("COMMAND:  open|close|init|update|final\n");

	LOG_INF("open USE_HC2  open the ecies context/core");
	LOG_INF("USE_HC2:      boolean (0 or 1), use HC2 or not\n");

	LOG_INF("close         close the ecies context/core\n");

	LOG_INF("init OP MODE PEERX PRIV");
	LOG_INF("OP:     encrypt|decrypt");
	LOG_INF("MODE:   int, ECIES mode. See cmrt_ecies_init api doc");
	LOG_INF("PEERX:  for encrypt, the recipient's pubkey, for decrypt,");
	LOG_INF("        the message pubkey, generated by the encrypt init");
	LOG_INF("PRIV:   (decrypt only) ECIES private key\n");

	LOG_INF("update DATA");
	LOG_INF("DATA:   data to encrypt/decrypt, length multiple of 16");
	LOG_INF("        You can perform 0 or more of this operation\n");

	LOG_INF("final TAGLEN DATA SHARED_INFO");
	LOG_INF("DATA:   last block of data to encrypt/decrypt");
	LOG_INF("        No length requirement\n");
}

/* Converts a (decimal) string into a uint32_t. Returns the parsed number.
 * Processes exactly `sl` bytes from the string `s`.
 * On conversion error sets errno to -EINVAL and returns 0.
 *
 * Encountering a nullbyte is a a conversion error.
 * An empty string (`sl`== 0) is a conversion error.
 * No wrapparound checks on the parsed number or on `sl`.
 * */
static uint32_t conv_strl2u32(const char *s, size_t sl)
{
	uint32_t acc = 0;
	const char *s_end = s + sl; /* no ovf check */
	for (; (size_t)s < (size_t)s_end; ++s) {
		char v = *s;
		if (v < '0' || v > '9') {
			errno = EINVAL;
			return 0;
		}
		v -= '0';
		acc *= 10; /* no ovf check */
		acc += v;
	}
	return acc;
}

static void debug_print_blob(uint8_t *blob, size_t sz, const char *info_text)
{
#ifdef CONFIG_DEBUG
#define _MAX_DBG_LEN 64
	if (!blob || !sz) return;

	size_t sz_hex = sz*2;
	uint8_t blob_hex[_MAX_DBG_LEN*2];
	size_t written = bin2hex(blob, MIN(sz, _MAX_DBG_LEN), (char*)blob_hex, _MAX_DBG_LEN*2);

	LOG_DBG("%s%.*s%s", info_text, (int)sz_hex, blob_hex,
		(written < sz_hex) ? "..." /*cropped*/ : "" );
#undef _MAX_DBG_LEN
#else
	(void)blob; (void)sz; (void)info_text;
#endif
}

static void ecies_close(void) {
	if (cmrt_is_valid(ecies)) {
		LOG_INF("Closing ECIES context");
		cmrt_ecies_close(ecies);

		ecies = CMRT_INVALID_HANDLE;
	}
}

